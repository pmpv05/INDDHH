//establecer el orden
function setOrderByClass(obj){
	obj.toggleClass("orderedBy");
	if(obj.hasClass("unsorted")){
		obj.removeClass("unsorted")
		obj.addClass("sortUp");
	} else {
		if(obj.hasClass("sortUp")){
			obj.removeClass("sortUp")
			obj.addClass("sortDown");
		}else{
			obj.addClass("sortUp")
			obj.removeClass("sortDown");
		}
	}
}

function removeOrderByClass(obj){
	$('orgRolesTrOrderBy').getElements(".orderedBy").each(function(item, index){
	    item.removeClass("orderedBy");
	});
	
	$('orgRolesTrOrderBy').getElements(".sortUp").each(function(item, index){
		if(obj!=item){
			item.removeClass("sortUp");
			item.addClass("unsorted");
		}
	});
	
	$('orgRolesTrOrderBy').getElements(".sortDown").each(function(item, index){
		if(obj!=item){
			item.removeClass("sortDown");
			item.addClass("unsorted");
		}
	});
	
}

function showOrgRolesModal(retFunction, closeFunction){
	
	if(ORG_ROL_MODAL_HIDE_OVERFLOW) {
		$(document.body).setStyle('overflow', 'hidden');
	}
	
	setFilterOrgRole();
	unSelectAllRows($('tableDataOrgRole'));

	var mdlOrgRoleContainer = $('mdlOrgRoleContainer');
	mdlOrgRoleContainer.removeClass('hiddenModal');
	mdlOrgRoleContainer.position();
	mdlOrgRoleContainer.blockerModal.show();
	mdlOrgRoleContainer.setStyle('zIndex', SYS_PANELS.getNewZIndex());
	mdlOrgRoleContainer.onModalConfirm = retFunction;
	mdlOrgRoleContainer.onModalClose = closeFunction;
}

function closeOrgRolesModal(){
	var mdlOrgRoleContainer = $('mdlOrgRoleContainer');
	
	if(Browser.chrome || Browser.firefox || Browser.safari) {
		var morph = new Fx.Morph(mdlOrgRoleContainer, {duration: 200, wait: false});
		
		morph.start({
			'margin-top': '-10px',
			'opacity': 0
		}).chain(function() {
			
			mdlOrgRoleContainer.addClass('hiddenModal');
			mdlOrgRoleContainer.setStyles({
				'opacity': 1,
				'margin-top': ''
			}); //Limpiar la transicion
						
			mdlOrgRoleContainer.blockerModal.hide();
			if (mdlOrgRoleContainer.onModalClose) mdlOrgRoleContainer.onModalClose();
			
			if(ORG_ROL_MODAL_HIDE_OVERFLOW) {
				$(document.body).setStyle('overflow', '');
			}
		});
	} else {
		
		mdlOrgRoleContainer.addClass('hiddenModal');
		
		mdlOrgRoleContainer.blockerModal.hide();
		if (mdlOrgRoleContainer.onModalClose) mdlOrgRoleContainer.onModalClose();
		
		if(ORG_ROL_MODAL_HIDE_OVERFLOW) {
			$(document.body).setStyle('overflow', '');
		}
	}
}
