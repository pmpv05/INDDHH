var ORG_ROL_MODAL_HIDE_OVERFLOW	= true;

function initOrgRoleMdlPage(){
	var mdlOrgRoleContainer = $('mdlOrgRoleContainer');
	if (mdlOrgRoleContainer.initDone) return;
	mdlOrgRoleContainer.initDone = true;
	
	mdlOrgRoleContainer.blockerModal = new Mask();
	
	['orderByNameOrgRoleMdl'].each(function(ele){
		ele = $(ele);
		ele.set('title', GNR_ORDER_BY + ele.getAttribute("title"))
	});
	
	['nameFilterOrgRoleMdl'].each(function(ele) {
		ele = $(ele);
		ele.setFilterOrgRole = setFilterOrgRole;
		ele.oldValue = ele.value;
		ele.addEvent("keyup", function(e) {
			if (this.oldValue == this.value) return;
			if (this.timmer) $clear(this.timmer);
			this.oldValue = this.value;
			this.timmer = this.setFilterOrgRole.delay(200, this);
			
		});		
	});
	
	$('closeModalOrgRole').addEvent("click", function(e) {
		e.stop();
		closeOrgRolesModal();
	});
	
	$('clearFiltersOrgRole').addEvent("click", function(e) {
		e.stop();
		['nameFilterOrgRoleMdl'].each(clearFilter);
		$('nameFilterOrgRoleMdl').setFilterPool();
	});
	
	$('confirmModalOrgRole').addEvent("click", function(e) {
		var mdlOrgRoleContainer = $('mdlOrgRoleContainer');
		if (mdlOrgRoleContainer.onModalConfirm) jsCaller(mdlOrgRoleContainer.onModalConfirm, getSelectedRows($('tableDataOrgRole')));
		closeOrgRolesModal();
	});
	
	//Evento doble-click con misma funcionalidad que boton confirmar
	$('tableDataOrgRole').getParent().addEvent("dblclick", function(e) {
		if (mdlOrgRoleContainer.onModalConfirm) jsCaller(mdlOrgRoleContainer.onModalConfirm,getSelectedRows($('tableDataOrgRole')));
		closeOrgRolesModal();
	});
	
	//eventos para order
	['orderByNameOrgRoleMdl'].each(function(ele){
		$(ele).addEvent("click", function(e) {
			e.stop();
			callNavigateOrder(this.getAttribute('data-sortBy'),this,'/apia.modals.OrganizationalRolesAction.run','OrgRole');
		});
	});
	
	window.sp_OrgRole = new Spinner($('tableDataOrgRole').getParent('table'), {message: WAIT_A_SECOND});
	window.sp_OrgRole.content.getParent().addClass('mdlSpinner');
	
	initNavButtons('/apia.modals.OrganizationalRolesAction.run','OrgRole');
}


 

